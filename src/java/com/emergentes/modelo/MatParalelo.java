package com.emergentes.modelo;

public class MatParalelo {
    
    private int id;
    private int id_materia;
    private int id_paralelo;
    private String mat_par;
   
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_materia() {
        return id_materia;
    }

    public void setId_materia(int id_materia) {
        this.id_materia = id_materia;
    }

    public int getId_paralelo() {
        return id_paralelo;
    }

    public void setId_paralelo(int id_paralelo) {
        this.id_paralelo = id_paralelo;
    }


    public String getMat_par() {
        return mat_par;
    }

    public void setMat_par(String mat_par) {
        this.mat_par = mat_par;
    }
    
    
}
